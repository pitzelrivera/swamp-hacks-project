class Player:
    def __init__(self, name, cards):
        self.name = name
        self.cards = list()
        self.money = 500
    
    def set_Name(self, input_Name):
        self.__name = input_Name

    def set_Money(self, input_Money):
        self.__money = input_Money

    def set_Cards(self, input_Cards):
        self.__cards = cards.append(input_Cards)

    def get_Name(self):
        return name

    def get_Money(self):
        return money
